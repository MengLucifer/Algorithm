# ─────────────────────────────────────────────
# Moving logo detector + remover
# ─────────────────────────────────────────────
# Detects a floating colorful logo that drifts around the frame
# (independent of the static drawbox masks above).
# Works on already-extracted JPEG frames (post-FFmpeg).
#
# Strategy:
#   1. Saturation mask  -- logo is thick and colorful (high HSV saturation)
#   2. Motion mask      -- logo moves independently from video content
#   3. Intersection     -- colorful AND moving = floating logo
#   4. Position history -- smooths detection across frames (logo moves slowly)
#   5. Inpainting       -- fills detected region with surrounding pixels
#
# All coordinates are relative to the CROPPED arena frame (1260x772).
# ─────────────────────────────────────────────

# Tuning knobs -- adjust if logo is not detected correctly
MOVING_LOGO_CONFIG = {
    "min_area":        500,    # ignore blobs smaller than this (px²)
    "max_area_ratio":  0.08,   # logo won't exceed 8% of frame area
    "sat_threshold":   80,     # HSV saturation threshold (0-255)
    "motion_threshold": 15,    # pixel diff threshold for motion mask
    "inpaint_radius":  5,      # inpainting brush radius (px)
    "history_size":    15,     # frames to remember for position smoothing
    "pad":             10,     # extra padding around detected bbox (px)
}


class MovingLogoDetector:
    """
    Per-video stateful detector for a floating colorful logo.
    Create one instance per video; call detect(frame) on each frame in order.
    """

    def __init__(self, cfg=None):
        c = cfg or MOVING_LOGO_CONFIG
        self.min_area       = c["min_area"]
        self.max_area_ratio = c["max_area_ratio"]
        self.sat_thresh     = c["sat_threshold"]
        self.motion_thresh  = c["motion_threshold"]
        self.history_size   = c["history_size"]
        self.pad            = c["pad"]
        self.prev_gray      = None
        self.history        = []   # recent (x, y, w, h) detections

    def detect(self, frame):
        """
        Returns (x, y, w, h) bounding box of the floating logo, or None.
        frame: BGR numpy array (already-cropped arena frame).
        """
        h, w = frame.shape[:2]
        max_area = w * h * self.max_area_ratio

        # ── Saturation mask: logo is thick + colorful ──
        hsv = cv2.cvtColor(frame, cv2.COLOR_BGR2HSV)
        _, sat_mask = cv2.threshold(
            hsv[:, :, 1], self.sat_thresh, 255, cv2.THRESH_BINARY)

        # ── Motion mask: logo moves independently ──
        gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
        if self.prev_gray is None:
            self.prev_gray = gray
            return None

        diff = cv2.absdiff(gray, self.prev_gray)
        self.prev_gray = gray.copy()
        _, motion_mask = cv2.threshold(
            diff, self.motion_thresh, 255, cv2.THRESH_BINARY)

        kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (15, 15))
        motion_mask = cv2.dilate(motion_mask, kernel, iterations=2)

        # ── Combine: must be colorful AND moving ──
        combined = cv2.bitwise_and(sat_mask, motion_mask)
        combined = cv2.morphologyEx(combined, cv2.MORPH_OPEN,
                                    np.ones((5, 5), np.uint8))
        combined = cv2.morphologyEx(combined, cv2.MORPH_CLOSE,
                                    np.ones((20, 20), np.uint8))

        # ── Find best contour ──
        contours, _ = cv2.findContours(
            combined, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

        best, best_score = None, 0
        for cnt in contours:
            area = cv2.contourArea(cnt)
            if area < self.min_area or area > max_area:
                continue
            bx, by, bw, bh = cv2.boundingRect(cnt)
            score = area
            # Prefer regions near historical logo position (logo drifts slowly)
            if self.history:
                avg_x = np.mean([r[0] for r in self.history])
                avg_y = np.mean([r[1] for r in self.history])
                dist  = np.hypot(bx - avg_x, by - avg_y)
                score += max(0, 5000 - dist * 10)
            if score > best_score:
                best_score = score
                best = (bx, by, bw, bh)

        if best:
            bx, by, bw, bh = best
            # Add padding, clamp to frame bounds
            bx = max(0, bx - self.pad)
            by = max(0, by - self.pad)
            bw = min(w - bx, bw + self.pad * 2)
            bh = min(h - by, bh + self.pad * 2)
            best = (bx, by, bw, bh)
            self.history.append(best)
            if len(self.history) > self.history_size:
                self.history.pop(0)

        return best


def remove_moving_logo(frame, bbox):
    """
    Inpaint (remove) the detected logo region from a frame.
    bbox: (x, y, w, h) or None.
    Returns the cleaned frame (or original if bbox is None).
    """
    if bbox is None:
        return frame
    x, y, w, h = bbox
    mask = np.zeros(frame.shape[:2], dtype=np.uint8)
    mask[y:y + h, x:x + w] = 255
    return cv2.inpaint(
        frame, mask,
        inpaintRadius=MOVING_LOGO_CONFIG["inpaint_radius"],
        flags=cv2.INPAINT_TELEA,
    )


def apply_moving_logo_removal(frame_data, remove_logo=True):
    """
    Post-process all extracted JPEG frames in frame_data:
      - Open each frame with OpenCV
      - Detect floating logo (stateful, sequential)
      - Inpaint logo region
      - Overwrite the JPEG in-place

    frame_data: list of {"path": str, "timestamp": float}
    remove_logo: set False to skip (pass-through)
    Returns counts: (cleaned, skipped, errors)
    """
    if not remove_logo or not frame_data:
        return 0, len(frame_data), 0

    detector = MovingLogoDetector()
    cleaned = skipped = errors = 0

    for fd in frame_data:
        try:
            frame = cv2.imread(fd["path"])
            if frame is None:
                errors += 1
                continue
            bbox         = detector.detect(frame)
            clean_frame  = remove_moving_logo(frame, bbox)
            cv2.imwrite(fd["path"], clean_frame,
                        [cv2.IMWRITE_JPEG_QUALITY, 95])
            if bbox:
                cleaned += 1
            else:
                skipped += 1
        except Exception as e:
            print("  WARNING: moving logo removal failed for {}: {}".format(
                fd["path"], e))
            errors += 1

    return cleaned, skipped, errors


def build_vf(extra, crop, masks=None):
    """
    Build FFmpeg -vf filter chain:
      1. Crop to arena region
      2. Draw black boxes over overlay zones (masks)
      3. Apply any extra filter (fps, scene detect, etc.)
    FFmpeg crop syntax:  crop=w:h:x:y
    FFmpeg drawbox syntax: drawbox=x:y:w:h:color=black:t=fill
    """
    c = crop
    filters = []

    # Step 1: crop to arena
    filters.append("crop={}:{}:{}:{}".format(c["width"], c["height"], c["x"], c["y"]))

    # Step 2: mask overlays with black boxes
    if masks:
        for m in masks:
            filters.append("drawbox=x={}:y={}:w={}:h={}:color=black:t=fill".format(
                m["x"], m["y"], m["w"], m["h"]
            ))

    # Step 3: extra filters (fps, scene, etc.)
    if extra:
        filters.append(extra)

    return ",".join(filters)


