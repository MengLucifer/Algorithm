def main():
    parser = argparse.ArgumentParser(
        description="Video QA Pre-Processor: Extract arena frames and compute perceptual hashes",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=r"""
Examples (Windows):
  # All videos in folder, 1fps (default, with overlay masking)
  python video_preprocess.py --input "D:\video" --batch

  # Disable overlay masking (raw arena frames)
  python video_preprocess.py --input "D:\video" --batch --no-mask

  # Add extra mask region (x:y:w:h)
  python video_preprocess.py --input "D:\video" --batch --mask 430:400:400:100

  # All videos, 2fps (higher accuracy)
  python video_preprocess.py --input "D:\video" --batch --fps 2

  # Scene-change mode (faster)
  python video_preprocess.py --input "D:\video" --batch --mode scene

  # Single video
  python video_preprocess.py --input "D:\video\recording1.mp4"

  # Custom output folder
  python video_preprocess.py --input "D:\video" --batch --output "D:\HashDB"

  # Override crop only if your video resolution is different from 1920x1040
  python video_preprocess.py --input "D:\video" --batch --crop 11:71:1260:772

  # Show system architecture diagram
  python video_preprocess.py --architecture
        """
    )

    parser.add_argument("--architecture", "-a", action="store_true",
                        help="Print the full system architecture diagram and exit")
    parser.add_argument("--input",  "-i", required=False, default=None,
                        help="Path to a video file or directory (use with --batch)")
    parser.add_argument("--output", "-o", default=None,
                        help="Output directory for JSON files (default: <input>\\hash_db)")
    parser.add_argument("--batch",  "-b", action="store_true",
                        help="Process all videos in the input directory")
    parser.add_argument("--mode",   "-m",
                        choices=["fps", "scene", "keyframe", "twopass"], default="fps",
                        help="Frame extraction mode (default: fps)")
    parser.add_argument("--fps",    "-f", type=float, default=1.0,
                        help="Frames per second for fps mode (default: 1)")
    parser.add_argument("--cores",  "-c", type=int, default=None,
                        help="CPU cores to use (default: auto = {})".format(CONFIG["num_cores"]))
    parser.add_argument("--crop", type=str, default=None,
                        help="Override arena crop as x:y:width:height "
                             "(default: 11:71:1260:772 for 1920x1040 video)")
    parser.add_argument("--no-mask", action="store_true",
                        help="Disable overlay masking (use raw arena frames without drawbox)")
    parser.add_argument("--no-logo", action="store_true",
                        help="Disable moving logo detection and inpainting")
    parser.add_argument("--mask", type=str, action="append", default=None,
                        help="Add/override mask region as x:y:w:h (can be used multiple times). "
                             "When used, REPLACES the built-in masks. "
                             "Coordinates are relative to the CROPPED arena.")

    args = parser.parse_args()

    # ── Show architecture and exit ──
    if args.architecture:
        print_architecture()
        sys.exit(0)

    # ── Require --input for actual processing ──
    if args.input is None:
        parser.error("--input is required (unless using --architecture)")

    # Apply core override
    if args.cores:
        CONFIG["num_cores"] = min(args.cores, cpu_count())

    # Parse crop override
    crop = None
    if args.crop:
        try:
            x, y, w, h = map(int, args.crop.split(":"))
            crop = {"x": x, "y": y, "width": w, "height": h}
        except Exception:
            print("ERROR: Invalid --crop. Use format x:y:width:height  e.g.  11:71:1260:772")
            sys.exit(1)

    # Resolve overlay masks
    if args.no_mask:
        masks = None
    elif args.mask:
        # User-specified masks replace built-in defaults
        masks = []
        for i, m_str in enumerate(args.mask):
            try:
                mx, my, mw, mh = map(int, m_str.split(":"))
                masks.append({"x": mx, "y": my, "w": mw, "h": mh, "label": "custom_{}".format(i + 1)})
            except Exception:
                print("ERROR: Invalid --mask '{}'. Use format x:y:w:h  e.g.  430:400:400:100".format(m_str))
                sys.exit(1)
    else:
        # Default: use built-in OVERLAY_MASKS
        masks = OVERLAY_MASKS

    # Default output directory
    if args.output is None:
        base = args.input if args.batch else str(Path(args.input).parent)
        args.output = str(Path(base) / "hash_db")

    c = crop or ARENA_CROP
    print("\nVIDEO QA PRE-PROCESSOR")
    print("   Input:       {}".format(args.input))
    print("   Output:      {}".format(args.output))
    print("   Mode:        {}".format(args.mode))
    print("   Cores:       {}".format(CONFIG["num_cores"]))
    print("   Arena crop:  x={}, y={}, {}x{} px (calibrated for 1920x1040)".format(
        c["x"], c["y"], c["width"], c["height"]))
    if masks:
        print("   Masks:       {} overlay regions".format(len(masks)))
        for mask in masks:
            print("                - {}: x={}, y={}, {}x{} px".format(
                mask.get("label", "unnamed"), mask["x"], mask["y"], mask["w"], mask["h"]))
    else:
        print("   Masks:       DISABLED")
    print("   Moving logo: {}".format("DISABLED (--no-logo)" if args.no_logo else "detection + inpainting ON"))
    if args.mode == "fps":
        print("   FPS:         {}".format(args.fps))
    print()

    if args.batch:
        if not os.path.isdir(args.input):
            print("ERROR: Not a directory: {}".format(args.input))
            sys.exit(1)
        process_batch(args.input, args.output, mode=args.mode, fps=args.fps, crop=crop, masks=masks, no_logo=args.no_logo)
    else:
        if not os.path.isfile(args.input):
            print("ERROR: File not found: {}".format(args.input))
            sys.exit(1)
        process_video(args.input, args.output, mode=args.mode, fps=args.fps, crop=crop, masks=masks, no_logo=args.no_logo)


if __name__ == "__main__":
    main()
